package com.obj.domain;

/**
 * Clase de entidad Category
 * @author Jrsaavedra
 *
 */
public class Category {
	// Atributos
	private Integer id;
    private String name;
    
    // Métodos getter and setter
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
